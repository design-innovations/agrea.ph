			<blockquote class="mb-6 text-sm font-agrea normal-case">AGREA is an innovative, inclusive, for purpose, agri-business that is founded on sustainable agriculture, fair-trade, and replicable model of an agri-based economy.</blockquote>

			<p>We are in the most essential business – feeding people. Rich or poor, young or old: everyone needs to eat. Food is essen- tial to life on Earth. Hence, dignifying all the stakeholders in agricultural systems, more importantly the farmers is the core of our work. Many farmers create vibrant livelihoods out of food production, while others struggle to get ahead. AGREA is a small player but a quality player. We founded our business on <span class="text-gray-700 font-bold">Ecology of Dignity</span> as agriculture is the only industry that takes care of all living things on earth: plants, animals and humans.</p>

			<p>We are focused on making the Philippine archipelago to be agriculturally sustainable built on the <span class="text-gray-700 font-bold">One Island Economy</span> model guided by three major goals: <span class="text-gray-700 font-bold">Zero Hunger, Zero Waste and Zero Insufficiency.</span> We believe that Filipino Farmers are world class. They are the point of change in the food system. We also continue nourishing every Filipino family. Investing in human capi- tal through nutrition is considered a sustainable economic investment. Nutritious and safe food allows for healthy and productive lives. It’s as important as the air we breathe and the water we drink.</p>

			<p>Healthy human capital is the backbone of our economy. Taking good care of our only planet is integrated in our business. Environmental impact is a key consideration in all our business operations.	Our approach is transformational, we make sure every transaction made with AGREA from producing to bringing food to the consumers is contributing to impactful changes in every point of the supply chain.</p>


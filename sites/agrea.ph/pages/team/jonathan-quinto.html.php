		<p>Born and raised in Torrijos, Marinduque, Jonathan is a fifth generation farmer and fisherman. After juggling his college studies and farming for eight years, he received his Diploma in Agricultural Technology and a Bachelor’s Degree in Agricultural Technology (DAT-BAT) at the Marinduque State College. He took an 11-month Occupational Internship in Agro Studies from the Mehadrin Tnuport Export, Moshav Hodaya, in Southern Israel.</p>
		<p>He was champion (Group Category) during the “El Liderato: First National Leadership Masters Challenge” in December 2014 in Camarines Sur. He was also an international delegate to the “Dux Gregis: First International Youth Leaders Convergence” in June 2015 in Ho Chi Minh City, Vietnam. Jonathan is also a recipient of the 2016 Outstanding Young Torrijoseño Award in recognition of his outstanding ability, accomplishments, and service to the community.</p>
		<p>He is a former president of the Student Council Federation and member of the Board of Trustees of the Marinduque State College. Currently, he is also a member of the Board of Directors of the Marinduque State College Credit Cooperative.</p>

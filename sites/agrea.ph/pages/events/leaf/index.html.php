		<h3 class="text-gray-900 font-medium font-title my-4">2018 Forum: Redefine &amp; Revitalize Agriculture</h3>
		<div class="w-12 h-1 bg-green-500 rounded mt-2 mb-4"></div>

		<p><strong>DATE: </strong>September 29 &amp; 30, 2018<br />
		<strong>VENUE: </strong>Taal Vista Hotel, Tagaytay, Philippines</p>

		<p>Through the lens of <strong>Agripreneurship</strong>, <strong>Agritech: Digital Agriculture</strong>, and <strong>Agritourism</strong>, how can a wide range of disciplines join forces to push innovative and sustainable solutions for agriculture in the 21st century?</p>

		<p>Whether you work on a farm, in a restaurant, or a tech hub, we are calling all agricultural advocates (emerging leaders and experienced mentors) to share their passions with a network that thinks outside the box.</p>

		<p><strong>Redefining + Revitalizing Agriculture</strong><br />
		The theme for LEAF 2018 is <em>Redefining + Revitalizing Agriculture. </em>It demonstrating that agriculture is a field with abundant possibilities on and off the farm. As the inaugural forum we want to express loud and clear: the future of farming is now. The collaborative experience will address topics across the wider spectrum of agriculture. These topics are:</p>

		<ul>
			<li><strong>Agripreneurship</strong>, turning farming into a creative and lucrative business.</li>
			<li><strong>Agritech</strong>, evolving the way we farm and improving the agricultural value chain through 21st-century science and technology.</li>
			<li><strong>Agritourism</strong>, integrating farms into our leisure and lifestyle, bringing us back to a greater understanding of our food sources and connecting us to the producers.</li>
		</ul>

